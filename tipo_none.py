"""
# Tipo 'None':

- O tipo de dado 'None' em Python representa o tipo 'sem tipo'
ou tipo 'vazio'. Porém, falar de tipo 'sem tipo' é mais
apropriado.

- O tipo 'None' é sempre especificado com a primeira letra
maiúscula.

- Quando utilizaremos o tipo None:
# Pode mos utilizar 'None' em Python quando queremos criar
uma variável e inicializá-la com um tipo sem tipo antes de
atribuirmos um valor final para a mesma.

- O tipo 'None' sempre será avaliado como 'False' em Python.

"""

numbers = None

print(f"numbers = {numbers}")
print(f"type(numbers) = {type(numbers)}")

print(15*'-')

numbers = 1.44, 1.34, 5.67,
print(f"numbers = {numbers}")
print(f"type(numbers) = {type(numbers)}")

print(15*'-')


