"""
# Programação Orientada a Objetos (POO) em Python:
- POO => É um paradigma de programação que utiliza
mapeamento de objetos do mundo realpara modelos
computacionais.
- Paradigma de programação => É a forma/metodologia utilizada
para pensar/desenvolver sistemas.

# Principais Elementos da Orientação a Objetos:

- Classe => Modelo de objeto do mundo real que é presentado
computacionalmente.
- Atributo => Características do objeto;
- Método => Comportamento do objeto (funções dentro de uma
classe);
- Construtor => Método especial utilizado para criar
um objeto;
- Objeto => É uma instância da classe.

Observação: A partir do momento que criamos a nossa classe,
estamos em essência definindo nossos próprios tipos de dados.


"""

# Tipos de dados padrão em Python:
number = 10
print(f"type(number) = {type(number)}")
print(15*'-')

name = 'Geek'
print(f"type(name) = {type(name)}")
print(15*'-')

# Definindo nossos próprios tipos de dados usando
# classes e orientação a objetos.


class Product:
	pass


